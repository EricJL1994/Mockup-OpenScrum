    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String showInputDialog = JOptionPane.showInputDialog(null, "New class");
        if (showInputDialog != null && !showInputDialog.equals("")) {
            MainFrame.classes.add(new Class_(showInputDialog));
        }
        classesModel = new DefaultListModel();
        for (Class_ class_ : MainFrame.classes) {
            classesModel.addElement(class_);
        }
        jList1.setModel(classesModel);
    }//GEN-LAST:event_jButton3ActionPerformed

