    private DefaultListModel classesModel;
    private DefaultListModel addedModel;

    private void personalInit() {
        classesModel = new DefaultListModel();

        for (Class_ class_ : MainFrame.classes) {
            classesModel.addElement(class_);
        }
        jList1.setModel(classesModel);

        addedModel = new DefaultListModel();

        UserStory story = (UserStory) MainFrame.list.getSelectedValue();
        if (!story.getClasses().isEmpty()) {
            for (Class_ class_ : story.getClasses()) {
                addedModel.addElement(class_);
            }
        }

        jList2.setModel(addedModel);
    }
